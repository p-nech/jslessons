/*
var window

Каков будет результат выполнения этого кода? Почему?
*/
function test() {

  alert( window );

  var window = 5;

  alert( window );
}

test();

/*
Ответ:

Сначала undefined, потом 5.
var обрабатывается в самом начале, поэтому он успеет обработаться до того, как в него занесется значение 5. Получаем локальную переменную window, не имеющую отношения к глобальной.
*/